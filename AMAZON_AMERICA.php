  <h2>Plaza de Armas de Iquitos</h2>
  <p>
    Head to the port city of Iquitos in Peru for a fascinating visit, including a stroll around the Plaza de Armas. This busy central plaza sits among European-styled buildings built during the historic rubber boom. Relax here in between your trips to the jungle and shopping along the open-street markets. Sit beside the fountain, admire the public garden, enjoy the shade of a palm tree, or check out the monument to those Peruvians who died during the 1879-83 Pacific War.
  </p>

