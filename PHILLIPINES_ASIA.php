        <p><b><font size="50">Nacpan Beach, El Nido</font></b><br><br>
        El Nido in Palawan is most famous for its paradise-like islands and lagoons, but a 45-minute ride from its main town takes you to another idyllic destination:
        <br>Nacpan Beach. Island hopping escapades in El Nido, including a visit to El Nido Big Lagoon and other top Palawan beaches should not be missed. 
        <br>But after a jam-packed day of visiting coves, white-sand beaches in El Nido, limestone cliffs, and other El Nido Palawan tourist spots, a trip to relaxing Nacpan Beach is highly recommended.</p>

