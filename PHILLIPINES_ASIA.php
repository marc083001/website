        <p><b><font size="50">Banaue Rice Terraces, Ifugao</font></b><br><br>
        Hand-chiseled some 2,000 years ago, the Banaue Rice Terraces in Banaue of Ifugao, North Luzon, is a remarkable destination and is truly one of the most beautiful places in the Philippines and regarded as one of the Philippines' iconic landmarks. 
        <br>Sit in a colorful jeepney as it cruises along winding roads in Banaue, and views of dramatic mud-walled rice terraces leave you in awe. 
        <br>The Ifugao people were highly skilled in carving both wood and mountainsides. The Banaue Rice Terraces is listed as a UNESCO World Heritage Site. 
        <br>It offers spectacular views of emerald hues during the rice-growing seasons of April-May and October-November. 
        <br>Banaue and its many natural attractions may pack a lot of tourists so you can make your way further to quieter Batad Rice Terraces and take a quick dip at its mighty Tappiya waterfalls. 
        <br>You can also explore nearby Baguio City or pick fresh strawberries in La Trinidad, Benguet.</p>
