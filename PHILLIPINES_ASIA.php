        <p><b><font size="50">Mayon Volcano, Albay, Bicol</font></b><br><br>
        One of the top natural landmarks in the Philippines and one of the most-visited tourist spots in Albay, it is already a wonderful experience seeing the volcano from afar. 
        <br>But if you are an adrenaline junkie, you can soak up all its beauty while gliding through a zipline in Lignon Hill or going on a Mayon Volcano tour via a Mayon ATV ride in Bicol. 
        <br>Visit the Cagsawa Ruins for another unique and historic viewing point of the Mayon Volcano. As a bonus, from Legazpi, you can take a side trip to Donsol, 
        <br>where you can enjoy swimming with whale sharks in their natural habitat.</p>

